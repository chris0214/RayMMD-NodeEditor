using System.Globalization;
using System.Text;
using RayMmdNodeEditor.Graph;

namespace RayMmdNodeEditor.Services;

public sealed class RayAdvancedMaterialCompiler
{
    private static readonly string[] AdvancedSlots = ["Albedo", "Alpha", "Normal", "Smoothness", "Metalness", "Specular", "Occlusion", "Emissive", "CustomA", "CustomB"];
    private static readonly string[] ShadingSlots = ["Color", "Add", "Multiply"];

    private readonly List<string> _messages = [];
    private readonly Dictionary<Guid, string> _textureNames = [];
    private readonly List<AdvancedTexture> _textures = [];
    private readonly Dictionary<string, LightControllerBinding> _lightControllers = new(StringComparer.OrdinalIgnoreCase);
    private readonly Dictionary<string, string> _controllerValues = new(StringComparer.OrdinalIgnoreCase);
    private NodeGraph _graph = new();
    private RayDocument _document = new();

    public RayAdvancedMaterialCompileResult Compile(RayDocument document)
    {
        _document = document;
        _graph = document.Graph;
        _messages.Clear();
        _textureNames.Clear();
        _textures.Clear();
        _lightControllers.Clear();
        _controllerValues.Clear();

        var output = _graph.Nodes.FirstOrDefault(node => node.Kind == NodeKind.RayMaterialOutput);
        if (output is null)
        {
            return new RayAdvancedMaterialCompileResult(false, string.Empty, new HashSet<string>(), [], ["Ray Material Output node is required."]);
        }

        var slotExpressions = new Dictionary<string, AdvancedExpression>(StringComparer.OrdinalIgnoreCase);
        foreach (var slot in AdvancedSlots)
        {
            var connection = _graph.FindInputConnection(output.Id, slot);
            if (connection is null)
            {
                continue;
            }

            var expression = ResolveConnection(connection, new HashSet<Guid>());
            if (expression is not null)
            {
                if (expression.RequiresShading)
                {
                    _messages.Add($"{slot} uses a Ray final-shading channel node. Connect it to Ray Shading Output instead.");
                }
                else
                {
                    slotExpressions[slot] = expression;
                }
            }
        }

        var shadingExpressions = new Dictionary<string, AdvancedExpression>(StringComparer.OrdinalIgnoreCase);
        var shadingOutput = _graph.Nodes.FirstOrDefault(node => node.Kind == NodeKind.RayShadingOutput);
        if (shadingOutput is not null)
        {
            foreach (var slot in ShadingSlots)
            {
                var connection = _graph.FindInputConnection(shadingOutput.Id, slot);
                if (connection is null)
                {
                    continue;
                }

                var expression = ResolveConnection(connection, new HashSet<Guid>());
                if (expression is not null)
                {
                    shadingExpressions[slot] = expression;
                }
            }
        }

        if (slotExpressions.Count == 0 && shadingExpressions.Count == 0)
        {
            return new RayAdvancedMaterialCompileResult(_messages.Count == 0, string.Empty, new HashSet<string>(), [], _messages.ToList());
        }

        var block = slotExpressions.Count == 0 ? string.Empty : BuildPatchBlock(slotExpressions);
        var shadingBlock = shadingExpressions.Count == 0 ? string.Empty : BuildShadingPatchBlock(shadingExpressions, shadingOutput);
        return new RayAdvancedMaterialCompileResult(
            _messages.Count == 0,
            block,
            slotExpressions.Keys.ToHashSet(StringComparer.OrdinalIgnoreCase),
            _textures.Select(texture => texture.File).Distinct(StringComparer.OrdinalIgnoreCase).ToList(),
            _messages.ToList(),
            shadingBlock,
            shadingExpressions.Keys.ToHashSet(StringComparer.OrdinalIgnoreCase));
    }

    private string BuildPatchBlock(IReadOnlyDictionary<string, AdvancedExpression> slots)
    {
        var builder = new StringBuilder();
        builder.AppendLine("// RAY_MMD_NODE_EDITOR_ADVANCED_BEGIN");
        builder.AppendLine("// Generated by RayMmdNodeEditor advanced node mode. Do not edit this block by hand.");
        foreach (var slot in AdvancedSlots)
        {
            builder.AppendLine($"#define RAY_NODE_ADVANCED_{slot.ToUpperInvariant()} {(slots.ContainsKey(slot) ? 1 : 0)}");
        }
        builder.AppendLine();

        foreach (var texture in _textures)
        {
            builder.AppendLine($"texture {texture.Name} <string ResourceName = \"{texture.File}\";>;");
            builder.AppendLine($"sampler {texture.Name}Samp = sampler_state");
            builder.AppendLine("{");
            builder.AppendLine($"\ttexture = {texture.Name};");
            builder.AppendLine("\tMINFILTER = TEXTURE_FILTER; MAGFILTER = TEXTURE_FILTER; MIPFILTER = TEXTURE_MIP_FILTER;");
            builder.AppendLine("\tADDRESSU = WRAP; ADDRESSV = WRAP; MAXANISOTROPY = TEXTURE_ANISOTROPY_LEVEL;");
            builder.AppendLine("};");
            builder.AppendLine();
        }

        foreach (var controller in _lightControllers.Values)
        {
            var controllerName = EscapeFxString(controller.ControllerName);
            builder.AppendLine($"float3 {controller.PositionName} : CONTROLOBJECT<string name = \"{controllerName}\"; string item = \"Position\";>;");
            builder.AppendLine($"float3 {controller.DirectionName} : CONTROLOBJECT<string name = \"{controllerName}\"; string item = \"Direction\";>;");
        }
        if (_lightControllers.Count > 0)
        {
            builder.AppendLine();
        }

        AppendControllerDeclarations(builder);

        AppendAdvancedHelpers(builder);

        AppendFunction(builder, "Albedo", "float3", slots, expression => $"saturate({ToFloat3(expression)})");
        AppendFunction(builder, "Alpha", "float", slots, expression => $"saturate({ToFloat(expression)})");
        AppendFunction(builder, "Normal", "float3", slots, expression => $"normalize({ToFloat3(expression)})");
        AppendFunction(builder, "Smoothness", "float", slots, expression =>
        {
            var value = $"saturate({ToFloat(expression)})";
            if (expression.SmoothnessConversion == SmoothnessConversion.LinearRoughness)
            {
                return $"saturate(1.0 - {value})";
            }

            return expression.SmoothnessConversion == SmoothnessConversion.Roughness
                ? $"RoughnessToSmoothness({value})"
                : value;
        });
        AppendFunction(builder, "Metalness", "float", slots, expression => $"saturate({ToFloat(expression)})");
        AppendFunction(builder, "Specular", "float3", slots, expression => $"saturate({ToFloat3(expression)})");
        AppendFunction(builder, "Occlusion", "float", slots, expression => $"saturate({ToFloat(expression)})");
        AppendFunction(builder, "Emissive", "float3", slots, expression => $"max(0, {ToFloat3(expression)})");
        AppendFunction(builder, "CustomA", "float", slots, expression => $"saturate({ToFloat(expression)})");
        AppendFunction(builder, "CustomB", "float3", slots, expression => $"saturate({ToFloat3(expression)})");
        builder.AppendLine("// RAY_MMD_NODE_EDITOR_ADVANCED_END");
        return builder.ToString();
    }

    private string BuildShadingPatchBlock(IReadOnlyDictionary<string, AdvancedExpression> slots, GraphNode? shadingOutput)
    {
        var builder = new StringBuilder();
        builder.AppendLine("// RAY_MMD_NODE_EDITOR_SHADING_BEGIN");
        builder.AppendLine("// Generated by RayMmdNodeEditor final shading channel mode. Do not edit this block by hand.");
        foreach (var slot in ShadingSlots)
        {
            builder.AppendLine($"#define RAY_NODE_SHADING_{slot.ToUpperInvariant()} {(slots.ContainsKey(slot) ? 1 : 0)}");
        }
        builder.AppendLine();

        foreach (var texture in _textures)
        {
            builder.AppendLine($"texture {texture.Name} <string ResourceName = \"{texture.File}\";>;");
            builder.AppendLine($"sampler {texture.Name}Samp = sampler_state");
            builder.AppendLine("{");
            builder.AppendLine($"\ttexture = {texture.Name};");
            builder.AppendLine("\tMINFILTER = TEXTURE_FILTER; MAGFILTER = TEXTURE_FILTER; MIPFILTER = TEXTURE_MIP_FILTER;");
            builder.AppendLine("\tADDRESSU = WRAP; ADDRESSV = WRAP; MAXANISOTROPY = TEXTURE_ANISOTROPY_LEVEL;");
            builder.AppendLine("};");
            builder.AppendLine();
        }

        AppendControllerDeclarations(builder);
        AppendShadingHelpers(builder);
        builder.AppendLine("float3 RayNode_ApplyShading(float2 coord, MaterialParam material, float3 rayNodeInputLighting, float3 diffuse, float3 specular, float3 V, float3 L, float4 screenPosition)");
        builder.AppendLine("{");
        builder.AppendLine("\tfloat3 lighting = rayNodeInputLighting;");
        var intensity = shadingOutput is null ? "1.0" : FormatScalar(ReadFloat(shadingOutput, "Intensity", 1.0));
        var mask = BuildShadingOutputMask(shadingOutput);
        if (slots.TryGetValue("Color", out var color))
        {
            var mode = shadingOutput is null ? "override" : ReadText(shadingOutput, "BlendMode", "Override").Trim().ToLowerInvariant();
            var blended = mode switch
            {
                "add" => $"(lighting + {ToFloat3(color)} * {intensity})",
                "multiply" => $"(lighting * lerp(float3(1.0, 1.0, 1.0), {ToFloat3(color)}, saturate({intensity})))",
                "screen" => $"(1.0 - (1.0 - lighting) * (1.0 - saturate({ToFloat3(color)} * {intensity})))",
                "overlay" => $"RayNode_BlendOverlay(lighting, saturate({ToFloat3(color)} * {intensity}))",
                "softlight" or "soft light" => $"RayNode_BlendSoftLight(lighting, saturate({ToFloat3(color)} * {intensity}))",
                "lumamask" or "luma mask" => $"lerp(lighting, {ToFloat3(color)}, saturate(dot({ToFloat3(color)}, float3(0.2126, 0.7152, 0.0722)) * {intensity}))",
                _ => $"lerp(lighting, {ToFloat3(color)}, saturate({intensity}))",
            };
            builder.AppendLine($"\tlighting = lerp(lighting, {blended}, saturate({mask}));");
        }
        if (slots.TryGetValue("Multiply", out var multiply))
        {
            builder.AppendLine($"\tlighting *= lerp(float3(1.0, 1.0, 1.0), max(0.0, {ToFloat3(multiply)}), saturate({mask}));");
        }
        if (slots.TryGetValue("Add", out var add))
        {
            builder.AppendLine($"\tlighting += {ToFloat3(add)} * saturate({mask});");
        }
        builder.AppendLine("\treturn max(0.0, lighting);");
        builder.AppendLine("}");
        builder.AppendLine("// RAY_MMD_NODE_EDITOR_SHADING_END");
        return builder.ToString();
    }

    private static void AppendFunction(StringBuilder builder, string slot, string returnType, IReadOnlyDictionary<string, AdvancedExpression> slots, Func<AdvancedExpression, string> body)
    {
        if (!slots.TryGetValue(slot, out var expression))
        {
            return;
        }

        var parameters = slot.Equals("Alpha", StringComparison.OrdinalIgnoreCase)
            ? "float2 uv"
            : "float2 uv, MaterialParam material";
        builder.AppendLine($"{returnType} RayNode_Get{slot}({parameters})");
        builder.AppendLine("{");
        builder.AppendLine($"\treturn {body(expression)};");
        builder.AppendLine("}");
        builder.AppendLine();
    }

