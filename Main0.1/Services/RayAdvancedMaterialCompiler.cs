    private static string ToFloat(AdvancedExpression expression)
    {
        return expression.Components == 1 ? expression.Code : $"({expression.Code}).x";
    }

    private static string ToFloat2(AdvancedExpression expression)
    {
        return expression.Components switch
        {
            1 => $"float2({expression.Code}, {expression.Code})",
            _ => $"({expression.Code}).xy",
        };
    }

    private static string ToFloat3(AdvancedExpression expression)
    {
        return expression.Components switch
        {
            1 => $"float3({expression.Code}, {expression.Code}, {expression.Code})",
            2 => $"float3(({expression.Code}).x, ({expression.Code}).y, 0.0)",
            _ => $"({expression.Code}).rgb",
        };
    }

    private static string ToFloat4(AdvancedExpression expression)
    {
        return expression.Components switch
        {
            1 => $"float4({expression.Code}, {expression.Code}, {expression.Code}, {expression.Code})",
            2 => $"float4(({expression.Code}).x, ({expression.Code}).y, 0.0, 0.0)",
            3 => $"float4(({expression.Code}).x, ({expression.Code}).y, ({expression.Code}).z, 0.0)",
            _ => expression.Code,
        };
    }

    private static string ReadText(GraphNode node, string name, string fallback)
    {
        return node.Properties.TryGetValue(name, out var value) && !string.IsNullOrWhiteSpace(value) ? value : fallback;
    }

    private static double ReadFloat(GraphNode node, string name, double fallback)
    {
        return node.Properties.TryGetValue(name, out var text) &&
               double.TryParse(text, NumberStyles.Float, CultureInfo.InvariantCulture, out var value)
            ? value
            : fallback;
    }

    private static string FormatScalar(double value) => value.ToString("0.######", CultureInfo.InvariantCulture);

