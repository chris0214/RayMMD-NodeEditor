    private SpecularTerms BuildSpecularTerms(GraphNode node, HashSet<Guid> visiting, string roughnessPin, double roughnessFallback)
    {
        var normal = ResolveInput(node, "Normal", visiting) ?? new AdvancedExpression("float4(material.normal, 0.0)", 4);
        var viewDir = ResolveInput(node, "ViewDir", visiting) ?? DefaultViewDirection();
        var lightDir = ResolveInput(node, "LightDir", visiting) ?? DefaultLightDirection();
        var roughness = ResolveInput(node, roughnessPin, visiting) ?? new AdvancedExpression(FormatScalar(ReadFloat(node, roughnessPin, roughnessFallback)), 1);
        var f0 = ResolveInput(node, "F0", visiting) ?? new AdvancedExpression(
            $"float4({FormatScalar(ReadFloat(node, "F0R", 0.04))}, {FormatScalar(ReadFloat(node, "F0G", 0.04))}, {FormatScalar(ReadFloat(node, "F0B", 0.04))}, 1.0)",
            4);
        var lightColor = ResolveInput(node, "LightColor", visiting) ?? new AdvancedExpression("float4(1.0, 1.0, 1.0, 1.0)", 4);
        var n = $"normalize({ToFloat3(normal)})";
        var v = $"normalize({ToFloat3(viewDir)})";
        var l = $"normalize({ToFloat3(lightDir)})";
        var h = $"normalize(({v}) + ({l}))";
        var rough = $"max(saturate({ToFloat(roughness)}), 0.001)";
        var a = $"max({rough} * {rough}, 0.001)";
        var a2 = $"({a} * {a})";
        var noL = $"saturate(dot({n}, {l}))";
        var noV = $"(saturate(abs(dot({n}, {v}))) + 0.0001)";
        var noH = $"saturate(dot({n}, {h}))";
        var voH = $"saturate(dot({v}, {h}))";
        var distribution = $"({a2} / max(3.14159265 * pow(({noH} * {noH}) * ({a2} - 1.0) + 1.0, 2.0), 0.0001))";
        var lambdaV = $"({noL} * sqrt(max(((-{noV} * {a2}) + {noV}) * {noV} + {a2}, 0.0001)))";
        var lambdaL = $"({noV} * sqrt(max(((-{noL} * {a2}) + {noL}) * {noL} + {a2}, 0.0001)))";
        var visibility = $"(0.5 / max({lambdaV} + {lambdaL}, 0.0001))";
        var fresnelCode = $"float4(({ToFloat3(f0)}) + (1.0 - ({ToFloat3(f0)})) * pow(1.0 - {voH}, 5.0), ({ToFloat4(f0)}).a)";
        var colorCode = $"float4({distribution} * {visibility} * ({fresnelCode}).rgb * {ToFloat3(lightColor)} * {noL}, 1.0)";
        var distributionExpr = AdvancedExpression.Combine(distribution, 1, normal, viewDir, lightDir, roughness);
        var visibilityExpr = AdvancedExpression.Combine(visibility, 1, normal, viewDir, lightDir, roughness);
        var fresnelExpr = AdvancedExpression.Combine(fresnelCode, 4, normal, viewDir, lightDir, f0);
        var colorExpr = AdvancedExpression.Combine(colorCode, 4, normal, viewDir, lightDir, roughness, f0, lightColor);
        return new SpecularTerms(colorExpr, distributionExpr, visibilityExpr, fresnelExpr);
    }

    private AdvancedExpression EvalClearCoat(GraphNode node, HashSet<Guid> visiting)
    {
        var color = ResolveInput(node, "Color", visiting) ?? new AdvancedExpression("float4(material.albedo, 1.0)", 4);
        var mask = ResolveInput(node, "Mask", visiting) ?? new AdvancedExpression("1.0", 1);
        var normal = ResolveInput(node, "Normal", visiting) ?? new AdvancedExpression("float4(material.normal, 0.0)", 4);
        var lightDir = ResolveInput(node, "LightDir", visiting) ?? DefaultLightDirection();
        var viewDir = ResolveInput(node, "ViewDir", visiting) ?? DefaultViewDirection();
        var strength = FormatScalar(ReadFloat(node, "Strength", 1.0));
        var gloss = FormatScalar(ReadFloat(node, "Gloss", 0.8));
        var f0 = FormatScalar(ReadFloat(node, "F0", 0.04));
