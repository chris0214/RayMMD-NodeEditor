    private AdvancedExpression EvalRayLightingMix(GraphNode node, HashSet<Guid> visiting)
    {
        var baseColor = ResolveInput(node, "BaseColor", visiting) ?? new AdvancedExpression("float4(material.albedo, 1.0)", 4);
        var lightFactor = ResolveInput(node, "LightFactor", visiting) ?? new AdvancedExpression("1.0", 1);
        var lightColor = ResolveInput(node, "LightColor", visiting) ?? new AdvancedExpression("float4(1.0, 1.0, 1.0, 1.0)", 4);
        var intensity = ResolveInput(node, "Intensity", visiting) ?? new AdvancedExpression(FormatScalar(ReadFloat(node, "Intensity", 1.0)), 1);
        var amount = $"saturate({ToFloat(lightFactor)}) * {ToFloat(intensity)}";
        var expression = ReadText(node, "BlendMode", "MultiplyAdd").Trim().ToLowerInvariant() switch
        {
            "add" => $"({ToFloat4(baseColor)} + float4({ToFloat3(lightColor)} * ({amount}), 0.0))",
            "multiply" => $"({ToFloat4(baseColor)} * float4(lerp(float3(1.0, 1.0, 1.0), {ToFloat3(lightColor)}, saturate({amount})), 1.0))",
            _ => $"({ToFloat4(baseColor)} * float4(lerp(float3(1.0, 1.0, 1.0), {ToFloat3(lightColor)}, saturate({ToFloat(lightFactor)})), 1.0) + float4({ToFloat3(lightColor)} * ({amount}), 0.0))",
        };
        return AdvancedExpression.Combine(expression, 4, baseColor, lightFactor, lightColor, intensity);
    }

