        var expression = operation switch
        {
            "LessThan" => $"(1.0 - step({bc}, {ac}))",
            "GreaterThan" => $"(1.0 - step({ac}, {bc}))",
            "LessEqual" => $"step({ac}, {bc})",
            "GreaterEqual" => $"step({bc}, {ac})",
            "Equal" => $"(1.0 - step({epsilon}, abs({ac} - {bc})))",
            "NotEqual" => $"step({epsilon}, abs({ac} - {bc}))",
            _ => "0.0",
        };
        return AdvancedExpression.Combine(expression, 1, a, b);
    }

