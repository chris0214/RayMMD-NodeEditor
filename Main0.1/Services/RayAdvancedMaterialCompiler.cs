    private AdvancedExpression EvalRayIblReflection(GraphNode node, string outputPin, HashSet<Guid> visiting)
    {
        var baseColor = ResolveInput(node, "BaseColor", visiting) ?? new AdvancedExpression("float4(material.albedo, 1.0)", 4);
        var mask = ResolveInput(node, "Mask", visiting) ?? new AdvancedExpression("1.0", 1);
        var diffuseStrength = FormatScalar(ReadFloat(node, "DiffuseStrength", 1.0));
        var specularStrength = FormatScalar(ReadFloat(node, "SpecularStrength", 1.0));
        var fresnelPower = FormatScalar(ReadFloat(node, "FresnelPower", 5.0));
        var fresnel = $"pow(1.0 - saturate(abs(dot(material.normal, normalize(V)))), {fresnelPower})";
        var amount = $"saturate({ToFloat(mask)})";
        var diffuse = $"(RayNode_IblDiffuse(coord, screenPosition) * {diffuseStrength})";
        var specular = $"(RayNode_IblSpecular(coord, screenPosition) * ({specularStrength} * lerp(1.0, {fresnel}, 0.75)))";
        var combined = $"float4(({ToFloat3(baseColor)} * ({diffuse}).rgb + ({specular}).rgb) * {amount}, 1.0)";
        return outputPin.Equals("Diffuse", StringComparison.OrdinalIgnoreCase)
            ? AdvancedExpression.Combine($"float4(({diffuse}).rgb * {amount}, 1.0)", 4, baseColor, mask) with { RequiresShading = true }
            : outputPin.Equals("Specular", StringComparison.OrdinalIgnoreCase)
                ? AdvancedExpression.Combine($"float4(({specular}).rgb * {amount}, 1.0)", 4, baseColor, mask) with { RequiresShading = true }
                : AdvancedExpression.Combine(combined, 4, baseColor, mask) with { RequiresShading = true };
    }

