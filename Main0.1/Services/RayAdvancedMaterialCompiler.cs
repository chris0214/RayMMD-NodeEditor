    private AdvancedExpression EvalUvRotate(GraphNode node, HashSet<Guid> visiting)
    {
        var uv = ResolveInput(node, "UV", visiting) ?? new AdvancedExpression("uv", 2);
        var angleInput = ResolveInput(node, "Angle", visiting);
        var centerInput = ResolveInput(node, "Center", visiting);
        var angle = angleInput is null ? FormatScalar(ReadFloat(node, "Angle", 0.0)) : ToFloat(angleInput);
        var center = centerInput is null ?
            $"float2({FormatScalar(ReadFloat(node, "CenterU", 0.5))}, {FormatScalar(ReadFloat(node, "CenterV", 0.5))})" :
            ToFloat2(centerInput);
        var p = $"(({ToFloat2(uv)}) - ({center}))";
        var rotated = $"float2(({p}).x * cos({angle}) - ({p}).y * sin({angle}), ({p}).x * sin({angle}) + ({p}).y * cos({angle}))";
        return new AdvancedExpression($"(({rotated}) + ({center}))", 2);
    }

    private AdvancedExpression EvalParallaxUv(GraphNode node, HashSet<Guid> visiting)
