            $"float4({FormatScalar(ReadFloat(node, "TintR", 1.0))}, {FormatScalar(ReadFloat(node, "TintG", 1.0))}, {FormatScalar(ReadFloat(node, "TintB", 1.0))}, 1.0)",
            4);
        var strength = ResolveInput(node, "ReflectionStrength", visiting) ?? new AdvancedExpression(FormatScalar(ReadFloat(node, "ReflectionStrength", 1.0)), 1);
        var occlusion = ResolveInput(node, "Occlusion", visiting) ?? new AdvancedExpression(FormatScalar(ReadFloat(node, "Occlusion", 1.0)), 1);

        if (outputPin.Equals("StandaloneColor", StringComparison.OrdinalIgnoreCase))
        {
            var terms = BuildSpecularTerms(node, visiting, "Roughness", 0.35);
            var color = $"float4({ToFloat3(terms.Color)} * {ToFloat3(tint)} * {ToFloat(strength)} * saturate({ToFloat(occlusion)}), 1.0)";
            return AdvancedExpression.Combine(color, 4, terms.Color, tint, strength, occlusion);
        }

        if (outputPin.Equals("Smoothness", StringComparison.OrdinalIgnoreCase))
        {
            return AdvancedExpression.Combine($"RoughnessToSmoothness(saturate({ToFloat(roughness)}))", 1, roughness);
        }

        if (outputPin.Equals("Metalness", StringComparison.OrdinalIgnoreCase))
        {
            return AdvancedExpression.Combine($"saturate({ToFloat(metalness)})", 1, metalness);
        }

        if (outputPin.Equals("Occlusion", StringComparison.OrdinalIgnoreCase))
        {
            return AdvancedExpression.Combine($"saturate({ToFloat(occlusion)})", 1, occlusion);
        }

        if (outputPin.Equals("Specular", StringComparison.OrdinalIgnoreCase))
        {
            return AdvancedExpression.Combine($"float4(saturate({ToFloat3(f0)} * {ToFloat3(tint)} * {ToFloat(strength)}), 1.0)", 4, f0, tint, strength);
        }

        if (IsCustomA(outputPin))
        {
            var customA = ResolveInput(node, "CustomA", visiting) ?? BuildBrdfBridgeCustomA(node, roughness);
            return AdvancedExpression.Combine($"saturate({ToFloat(customA)})", 1, customA);
        }

        var customB = ResolveInput(node, "CustomB", visiting) ?? BuildBrdfBridgeCustomB(node, tint);
        return AdvancedExpression.Combine($"saturate({ToFloat4(customB)})", 4, customB);
    }

