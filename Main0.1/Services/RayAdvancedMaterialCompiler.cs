        var diffuse = $"((RayNode_IblDiffuse(coord, screenPosition)).rgb * {diffuseStrength})";
        var specular = $"((RayNode_IblSpecular(coord, screenPosition)).rgb * {specularStrength})";
        var diffuseLuma = $"dot({diffuse}, float3(0.299, 0.587, 0.114))";
        var specularLuma = $"dot({specular}, float3(0.299, 0.587, 0.114))";
        var fresnel = $"pow(1.0 - saturate(abs(dot(material.normal, normalize(V)))), {fresnelPower})";
        var result = outputPin.ToLowerInvariant() switch
        {
            "diffuse" => $"float4({diffuse} * {mask}, 1.0)",
            "specular" => $"float4({specular} * {mask}, 1.0)",
            "diffuseluma" => $"saturate({diffuseLuma} * {mask})",
            "specularluma" => $"saturate({specularLuma} * {mask})",
            "reflectionmask" => $"saturate({specularLuma} * {fresnel} * {mask})",
            _ => $"float4(({diffuse} + {specular}) * {mask}, 1.0)",
        };
        var components = outputPin.Contains("Luma", StringComparison.OrdinalIgnoreCase) || outputPin.Equals("ReflectionMask", StringComparison.OrdinalIgnoreCase)
            ? 1
            : 4;
        return AdvancedExpression.Combine(result, components, maskInput) with { RequiresShading = true };
    }

