    private AdvancedExpression EvalSlopeMask(GraphNode node, HashSet<Guid> visiting)
    {
        var normal = ResolveInput(node, "Normal", visiting) ?? new AdvancedExpression("float4(material.normal, 0.0)", 4);
        var direction = $"normalize(float3({FormatScalar(ReadFloat(node, "DirectionX", 0.0))}, {FormatScalar(ReadFloat(node, "DirectionY", 1.0))}, {FormatScalar(ReadFloat(node, "DirectionZ", 0.0))}))";
