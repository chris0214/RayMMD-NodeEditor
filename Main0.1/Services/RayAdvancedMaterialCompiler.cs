        builder.AppendLine("\tfloat2 brickUv = float2(p.x + fmod(row, 2.0) * rowOffset * brickWidth, p.y);");
        builder.AppendLine("\tfloat2 cell = frac(brickUv / max(float2(brickWidth, brickHeight), 0.00001));");
        builder.AppendLine("\tfloat edge = min(min(cell.x, 1.0 - cell.x) * brickWidth, min(cell.y, 1.0 - cell.y) * brickHeight);");
        builder.AppendLine("\treturn smoothstep(0.0, mortar, edge);");
        builder.AppendLine("}");
        builder.AppendLine();
    }

    private static string BuildShadingOutputMask(GraphNode? shadingOutput)
    {
        if (shadingOutput is null)
        {
            return "1.0";
        }

        var source = ReadText(shadingOutput, "MaskSource", "Constant").Trim().ToLowerInvariant();
        var mask = FormatScalar(ReadFloat(shadingOutput, "Mask", 1.0));
        return source switch
        {
            "ssao" or "ssdo" => $"({mask} * RayNode_ShadingSsao(coord))",
            "shadow" => $"({mask} * RayNode_ShadowFactor(coord))",
            "depthfade" or "depth fade" => $"({mask} * saturate(tex2Dlod(Gbuffer8Map, float4(coord, 0, 0)).r / 100.0))",
            "fresnel" => $"({mask} * pow(1.0 - saturate(abs(dot(material.normal, normalize(V)))), 5.0))",
            _ => mask,
        };
    }

