    private sealed record AdvancedExpression(string Code, int Components, SmoothnessConversion SmoothnessConversion = SmoothnessConversion.None, bool RequiresShading = false)
    {
        public static AdvancedExpression Combine(string code, int components, params AdvancedExpression[] expressions)
        {
            var conversion = expressions.Any(expression => expression.SmoothnessConversion == SmoothnessConversion.LinearRoughness)
                ? SmoothnessConversion.LinearRoughness
                : expressions.Any(expression => expression.SmoothnessConversion == SmoothnessConversion.Roughness)
                    ? SmoothnessConversion.Roughness
                    : SmoothnessConversion.None;
            return new AdvancedExpression(code, components, conversion, expressions.Any(expression => expression.RequiresShading));
        }
    }

    private enum SmoothnessConversion
    {
        None,
        Roughness,
        LinearRoughness,
    }
