    private AdvancedExpression EvalColorRamp(GraphNode node, HashSet<Guid> visiting)
    {
        var a = ResolveInput(node, "A", visiting) ?? new AdvancedExpression(
            $"float4({FormatScalar(ReadFloat(node, "StartR", 0.0))}, {FormatScalar(ReadFloat(node, "StartG", 0.0))}, {FormatScalar(ReadFloat(node, "StartB", 0.0))}, {FormatScalar(ReadFloat(node, "StartA", 1.0))})",
            4);
        var b = ResolveInput(node, "B", visiting) ?? new AdvancedExpression(
            $"float4({FormatScalar(ReadFloat(node, "EndR", 1.0))}, {FormatScalar(ReadFloat(node, "EndG", 1.0))}, {FormatScalar(ReadFloat(node, "EndB", 1.0))}, {FormatScalar(ReadFloat(node, "EndA", 1.0))})",
            4);
        var t = ResolveInput(node, "T", visiting) ?? new AdvancedExpression("0.5", 1);
        var start = FormatScalar(ReadFloat(node, "Start", 0.0));
        var end = FormatScalar(ReadFloat(node, "End", 1.0));
        var factor = $"saturate(({ToFloat(t)} - {start}) / max(0.00001, {end} - {start}))";
        var mode = ReadText(node, "Mode", "Linear").Trim().ToLowerInvariant();
        var stopCount = Math.Clamp((int)Math.Round(ReadFloat(node, "StopCount", 2.0)), 2, 5);
        if (stopCount > 2)
        {
            var stops = BuildAdvancedRampStops(node, stopCount, ToFloat4(a), ToFloat4(b));
            var expression = BuildAdvancedRampExpression(factor, stops, mode);
            return AdvancedExpression.Combine(expression, 4, a, b, t);
        }

        if (mode is "constant" or "step")
        {
            return AdvancedExpression.Combine($"(({factor}) < 1.0 ? {ToFloat4(a)} : {ToFloat4(b)})", 4, a, b, t);
        }

        if (mode is "smooth" or "ease" or "easing")
        {
            factor = $"smoothstep(0.0, 1.0, {factor})";
        }

        return AdvancedExpression.Combine($"lerp({ToFloat4(a)}, {ToFloat4(b)}, {factor})", 4, a, b, t);
    }

    private static List<RampStopExpression> BuildAdvancedRampStops(GraphNode node, int stopCount, string startColor, string endColor)
    {
        var stops = new List<RampStopExpression>
        {
            new(0.0, startColor),
        };
        if (stopCount >= 3)
        {
            stops.Add(new(
                ReadFloat(node, "Mid1", 0.25),
                $"float4({FormatScalar(ReadFloat(node, "Mid1R", 0.25))}, {FormatScalar(ReadFloat(node, "Mid1G", 0.25))}, {FormatScalar(ReadFloat(node, "Mid1B", 0.25))}, {FormatScalar(ReadFloat(node, "Mid1A", 1.0))})"));
        }

        if (stopCount >= 4)
        {
            stops.Add(new(
                ReadFloat(node, "Mid2", 0.5),
                $"float4({FormatScalar(ReadFloat(node, "Mid2R", 0.5))}, {FormatScalar(ReadFloat(node, "Mid2G", 0.5))}, {FormatScalar(ReadFloat(node, "Mid2B", 0.5))}, {FormatScalar(ReadFloat(node, "Mid2A", 1.0))})"));
        }

        if (stopCount >= 5)
        {
            stops.Add(new(
                ReadFloat(node, "Mid3", 0.75),
                $"float4({FormatScalar(ReadFloat(node, "Mid3R", 0.75))}, {FormatScalar(ReadFloat(node, "Mid3G", 0.75))}, {FormatScalar(ReadFloat(node, "Mid3B", 0.75))}, {FormatScalar(ReadFloat(node, "Mid3A", 1.0))})"));
        }

        stops.Add(new(1.0, endColor));
        return stops
            .Select(stop => stop with { Position = Math.Clamp(stop.Position, 0.0, 1.0) })
            .OrderBy(stop => stop.Position)
            .ToList();
    }

    private static string BuildAdvancedRampExpression(string factor, IReadOnlyList<RampStopExpression> stops, string mode)
    {
        if (mode is "constant" or "step")
        {
            var expression = stops[^1].Color;
            for (var index = stops.Count - 2; index >= 0; index--)
            {
                var boundary = FormatScalar(stops[index + 1].Position);
                expression = $"(({factor}) < {boundary} ? {stops[index].Color} : {expression})";
            }

            return expression;
        }

        var smooth = mode is "smooth" or "ease" or "easing";
        var result = BuildAdvancedRampSegment(factor, stops[^2], stops[^1], smooth);
        for (var index = stops.Count - 3; index >= 0; index--)
        {
            var boundary = FormatScalar(stops[index + 1].Position);
            var segment = BuildAdvancedRampSegment(factor, stops[index], stops[index + 1], smooth);
            result = $"(({factor}) < {boundary} ? {segment} : {result})";
        }

        return result;
    }

    private static string BuildAdvancedRampSegment(string factor, RampStopExpression left, RampStopExpression right, bool smooth)
    {
        var local = $"saturate(({factor} - {FormatScalar(left.Position)}) / max(0.00001, {FormatScalar(right.Position - left.Position)}))";
        if (smooth)
        {
            local = $"smoothstep(0.0, 1.0, {local})";
        }

        return $"lerp({left.Color}, {right.Color}, {local})";
    }

