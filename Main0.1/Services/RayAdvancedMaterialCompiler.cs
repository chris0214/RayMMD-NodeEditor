        builder.AppendLine("\treturn saturate(value);");
        builder.AppendLine("}");
        builder.AppendLine();
        builder.AppendLine("float RayNode_Brick(float2 p, float brickWidth, float brickHeight, float mortar, float rowOffset)");
        builder.AppendLine("{");
