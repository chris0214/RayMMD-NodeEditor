    private AdvancedExpression EvalTriplanarBoxmap(GraphNode node, string outputPin, HashSet<Guid> visiting)
    {
        var position = ResolveInput(node, "Position", visiting) ?? new AdvancedExpression("float4(uv.x, uv.y, 0.0, 1.0)", 4);
        var normal = ResolveInput(node, "Normal", visiting) ?? new AdvancedExpression("float4(material.normal, 0.0)", 4);
        var scale = FormatScalar(ReadFloat(node, "Scale", 1.0));
        var sharpness = FormatScalar(ReadFloat(node, "BlendSharpness", 4.0));
        string sample;
        if (_graph.FindInputConnection(node.Id, "Texture") is not null)
        {
            var texture = ResolveInput(node, "Texture", visiting) ?? new AdvancedExpression("float4(1.0, 1.0, 1.0, 1.0)", 4);
            sample = ToFloat4(texture);
        }
        else
        {
            var name = RegisterNodeTexture(node, ReadText(node, "ResourceName", "boxmap.png"), ReadText(node, "SourcePath", string.Empty));
            var p = $"({ToFloat3(position)} * {scale})";
            var n = $"pow(abs(normalize({ToFloat3(normal)})), {sharpness})";
            var w = $"(({n}) / max(dot({n}, float3(1.0, 1.0, 1.0)), 0.00001))";
            sample = $"(tex2D({name}Samp, ({p}).yz).rgba * ({w}).x + tex2D({name}Samp, ({p}).xz).rgba * ({w}).y + tex2D({name}Samp, ({p}).xy).rgba * ({w}).z)";
        }

        return TextureChannelOutput(outputPin, sample);
    }

    private static AdvancedExpression TextureFactorOutput(string outputPin, string factor)
    {
        return outputPin.Equals("Color", StringComparison.OrdinalIgnoreCase)
            ? new AdvancedExpression($"float4({factor}, {factor}, {factor}, 1.0)", 4)
            : new AdvancedExpression(factor, 1);
    }

    private static AdvancedExpression TextureChannelOutput(string outputPin, string sample)
    {
        return outputPin.ToUpperInvariant() switch
        {
            "RGB" => new AdvancedExpression($"({sample}).rgb", 3),
            "R" => new AdvancedExpression($"({sample}).r", 1),
            "G" => new AdvancedExpression($"({sample}).g", 1),
            "B" => new AdvancedExpression($"({sample}).b", 1),
            "A" => new AdvancedExpression($"({sample}).a", 1),
            _ => new AdvancedExpression(sample, 4),
        };
    }

    private string RegisterNodeTexture(GraphNode node, string resourceName, string sourcePath)
    {
        if (_textureNames.TryGetValue(node.Id, out var name))
        {
            return name;
        }

        name = $"RayNodeTex{_textures.Count}";
        _textureNames[node.Id] = name;
        var file = string.IsNullOrWhiteSpace(sourcePath) ? resourceName : sourcePath;
        _textures.Add(new AdvancedTexture(name, NormalizeFxPath(file)));
        return name;
    }

    private string RegisterControllerValue(string controllerName, string item)
    {
        var key = $"{controllerName}|{item}";
        if (_controllerValues.TryGetValue(key, out var name))
        {
            return name;
        }

        name = $"RayNodeCtrl{_controllerValues.Count}";
        _controllerValues[key] = name;
        return name;
    }

    private void AppendControllerDeclarations(StringBuilder builder)
    {
        foreach (var pair in _controllerValues)
        {
            var parts = pair.Key.Split('|', 2);
            var controllerName = EscapeFxString(parts[0]);
            var item = EscapeFxString(parts.Length > 1 ? parts[1] : "Value");
            builder.AppendLine($"float {pair.Value} : CONTROLOBJECT<string name = \"{controllerName}\"; string item = \"{item}\";>;");
        }

        if (_controllerValues.Count > 0)
        {
            builder.AppendLine();
        }
    }

