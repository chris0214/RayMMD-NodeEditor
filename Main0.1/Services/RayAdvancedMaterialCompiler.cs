    private AdvancedExpression EvalNormalMap(GraphNode node, HashSet<Guid> visiting)
    {
        var uvInput = ResolveInput(node, "UV", visiting) ?? new AdvancedExpression("uv", 2);
        var baseNormal = ResolveInput(node, "Normal", visiting) ?? new AdvancedExpression("float4(material.normal, 0.0)", 4);
        var name = RegisterNodeTexture(node, ReadText(node, "ResourceName", "normal.png"), ReadText(node, "SourcePath", string.Empty));
        var strength = FormatScalar(ReadFloat(node, "Strength", 1.0));
        var ySign = ReadText(node, "InvertY", "False").Equals("True", StringComparison.OrdinalIgnoreCase) ? "-1.0" : "1.0";
        var sample = $"tex2D({name}Samp, {ToFloat2(uvInput)})";
        var unpacked = $"normalize(float3(({sample}).r * 2.0 - 1.0, (({sample}).g * 2.0 - 1.0) * {ySign}, ({sample}).b * 2.0 - 1.0))";
        var blended = $"normalize(lerp({ToFloat3(baseNormal)}, {unpacked}, saturate({strength})))";
        return AdvancedExpression.Combine($"float4({blended}, 0.0)", 4, uvInput, baseNormal);
    }

    private AdvancedExpression EvalDetailNormalBlend(GraphNode node, HashSet<Guid> visiting)
    {
        var baseNormal = ResolveInput(node, "BaseNormal", visiting) ?? new AdvancedExpression("float4(material.normal, 0.0)", 4);
        var detailNormal = ResolveInput(node, "DetailNormal", visiting) ?? new AdvancedExpression("float4(0.0, 0.0, 1.0, 0.0)", 4);
        var strength = ResolveInput(node, "Strength", visiting) ?? new AdvancedExpression(FormatScalar(ReadFloat(node, "Strength", 1.0)), 1);
        var blended = $"normalize({ToFloat3(baseNormal)} + ({ToFloat3(detailNormal)} - float3(0.0, 0.0, 1.0)) * {ToFloat(strength)})";
        return AdvancedExpression.Combine($"float4({blended}, 0.0)", 4, baseNormal, detailNormal, strength);
    }

