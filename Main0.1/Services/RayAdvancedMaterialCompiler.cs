        if (ReadText(node, "Invert", "False").Equals("True", StringComparison.OrdinalIgnoreCase))
        {
            fade = $"(1.0 - {fade})";
        }

        return AdvancedExpression.Combine(fade, 1, sceneDepth, distance);
    }

    private AdvancedExpression EvalBoxMask(GraphNode node, HashSet<Guid> visiting)
    {
        var position = ResolveInput(node, "Position", visiting) ?? new AdvancedExpression("float4(uv.x, uv.y, 0.0, 1.0)", 4);
        var center = $"float3({FormatScalar(ReadFloat(node, "CenterX", 0.0))}, {FormatScalar(ReadFloat(node, "CenterY", 0.0))}, {FormatScalar(ReadFloat(node, "CenterZ", 0.0))})";
        var size = $"float3({FormatScalar(ReadFloat(node, "SizeX", 1.0))}, {FormatScalar(ReadFloat(node, "SizeY", 1.0))}, {FormatScalar(ReadFloat(node, "SizeZ", 1.0))})";
        var softness = FormatScalar(ReadFloat(node, "Softness", 0.05));
        var q = $"abs({ToFloat3(position)} - {center}) / max({size} * 0.5, 0.00001)";
        var outside = $"max(max(({q}).x, ({q}).y), ({q}).z)";
        var mask = $"(1.0 - smoothstep(1.0 - {softness}, 1.0 + {softness}, {outside}))";
        if (ReadText(node, "Invert", "False").Equals("True", StringComparison.OrdinalIgnoreCase))
        {
            mask = $"(1.0 - {mask})";
        }
        return AdvancedExpression.Combine(mask, 1, position);
    }

