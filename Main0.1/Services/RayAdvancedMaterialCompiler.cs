    private AdvancedExpression? ResolveConnection(GraphConnection connection, HashSet<Guid> visiting)
    {
        var node = _graph.FindNode(connection.SourceNodeId);
        return node is null ? null : Evaluate(node, connection.SourcePin, visiting);
    }

    private AdvancedExpression? ResolveInput(GraphNode node, string pin, HashSet<Guid> visiting)
    {
        var connection = _graph.FindInputConnection(node.Id, pin);
        return connection is null ? null : ResolveConnection(connection, visiting);
    }

    private AdvancedExpression? Evaluate(GraphNode node, string outputPin, HashSet<Guid> visiting)
    {
        if (!visiting.Add(node.Id))
        {
            _messages.Add("Advanced expression contains a connection cycle.");
            return null;
        }

        try
        {
            return node.Kind switch
            {
