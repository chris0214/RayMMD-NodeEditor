    private AdvancedExpression EvalBurleyDiffuse(GraphNode node, HashSet<Guid> visiting)
    {
        var albedo = ResolveInput(node, "Albedo", visiting) ?? new AdvancedExpression("float4(material.albedo, 1.0)", 4);
        var normal = ResolveInput(node, "Normal", visiting) ?? new AdvancedExpression("float4(material.normal, 0.0)", 4);
        var viewDir = ResolveInput(node, "ViewDir", visiting) ?? DefaultViewDirection();
        var lightDir = ResolveInput(node, "LightDir", visiting) ?? DefaultLightDirection();
        var roughness = ResolveInput(node, "Roughness", visiting) ?? new AdvancedExpression(FormatScalar(ReadFloat(node, "Roughness", 0.5)), 1);
        var lightColor = ResolveInput(node, "LightColor", visiting) ?? new AdvancedExpression("float4(1.0, 1.0, 1.0, 1.0)", 4);
        var n = $"normalize({ToFloat3(normal)})";
        var v = $"normalize({ToFloat3(viewDir)})";
        var l = $"normalize({ToFloat3(lightDir)})";
        var h = $"normalize(({v}) + ({l}))";
        var noL = $"saturate(dot({n}, {l}))";
        var noV = $"saturate(abs(dot({n}, {v})))";
        var loH = $"saturate(dot({l}, {h}))";
        var fd90 = $"(0.5 + 2.0 * {loH} * {loH} * saturate({ToFloat(roughness)}))";
        var scatterL = $"(1.0 + ({fd90} - 1.0) * pow(1.0 - {noL}, 5.0))";
        var scatterV = $"(1.0 + ({fd90} - 1.0) * pow(1.0 - {noV}, 5.0))";
        return AdvancedExpression.Combine($"float4({ToFloat3(albedo)} * {ToFloat3(lightColor)} * ({noL} * {scatterL} * {scatterV}), ({ToFloat4(albedo)}).a)", 4, albedo, normal, viewDir, lightDir, roughness, lightColor);
    }

