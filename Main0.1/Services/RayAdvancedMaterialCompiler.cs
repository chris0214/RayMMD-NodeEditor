        builder.AppendLine("\tfloat b = RayNode_Hash21(i + float2(1.0, 0.0));");
        builder.AppendLine("\tfloat c = RayNode_Hash21(i + float2(0.0, 1.0));");
        builder.AppendLine("\tfloat d = RayNode_Hash21(i + float2(1.0, 1.0));");
        builder.AppendLine("\tfloat2 u = f * f * (3.0 - 2.0 * f);");
        builder.AppendLine("\treturn lerp(lerp(a, b, u.x), lerp(c, d, u.x), u.y);");
