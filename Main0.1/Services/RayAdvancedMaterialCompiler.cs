    private AdvancedExpression? EvaluateTexture(GraphNode node, HashSet<Guid> visiting)
    {
        var source = ReadText(node, "Source", "File");
        if (!RayCompatibilityChecker.IsFileSource(source))
        {
            _messages.Add("Ray Texture Slot in advanced mode currently supports Source=File only. Use compatible parameters for PMX/Sphere/Toon sources.");
            return null;
        }

        var file = NormalizeFxPath(ReadText(node, "File", "texture.png"));
        if (!_textureNames.TryGetValue(node.Id, out var name))
        {
            name = $"RayNodeTex{_textures.Count}";
            _textureNames[node.Id] = name;
            _textures.Add(new AdvancedTexture(name, file));
        }

        var uv = ResolveInput(node, "UV", visiting) ?? new AdvancedExpression("uv", 2);
        var coord = ToFloat2(uv);
        coord = ApplyUvFlip(coord, ReadText(node, "UvFlip", "None"));
        var loop = $"float2({FormatScalar(ReadFloat(node, "LoopX", 1.0))}, {FormatScalar(ReadFloat(node, "LoopY", 1.0))})";
        var conversion = ReadText(node, "MapType", "Default").Trim().ToLowerInvariant() switch
        {
            "roughness" or "roughnesssqrt" => SmoothnessConversion.Roughness,
            "linearroughness" or "roughnesslinear" => SmoothnessConversion.LinearRoughness,
            _ => SmoothnessConversion.None,
        };
        return new AdvancedExpression($"tex2D({name}Samp, ({coord}) * {loop})", 4, conversion);
    }

    private AdvancedExpression EvalEmissivePulse(GraphNode node, string outputPin, HashSet<Guid> visiting)
    {
        var color = ResolveInput(node, "Color", visiting) ?? new AdvancedExpression("float4(1.0, 1.0, 1.0, 1.0)", 4);
        var timeInput = ResolveInput(node, "Time", visiting);
        var timeValue = timeInput is null ? "time" : ToFloat(timeInput);
        var speed = FormatScalar(ReadFloat(node, "Speed", 1.0));
        var phase = FormatScalar(ReadFloat(node, "Phase", 0.0));
        var min = FormatScalar(ReadFloat(node, "Min", 0.2));
        var max = FormatScalar(ReadFloat(node, "Max", 1.0));
        var factor = $"lerp({min}, {max}, 0.5 + 0.5 * sin((({timeValue}) * {speed} + {phase}) * 6.2831853))";
        return outputPin.Equals("Factor", StringComparison.OrdinalIgnoreCase)
            ? new AdvancedExpression(factor, 1)
            : AdvancedExpression.Combine($"({ToFloat4(color)} * {factor})", 4, color);
    }

